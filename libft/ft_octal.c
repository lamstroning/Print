#include "libft.h"

int		ft_octal(unsigned long long int o)
{
	int		module[15];
	int		i;

	i = 0;
	while (o > 7)
	{
		module[i++] = o % 8;
		o /= 8;
	}
	while (i > 0)
		o = o * 10 + module[--i];
	return (o);
}
